#include <stdio.h>
#include <stdlib.h>
#include <string.h>

#include "preprocessor.h"

static const char *process_source(const char *source) {
    char *p = malloc(strlen(source) + 1);
    char *s = p;
    int i = 0;

    for (char c = *source; c;) {
        if (c == ';') {
            while (c != '\n' && c != '\0') {
                c = *++source;
            }
        }
        if (c == '\\') {
            c = *++source;
            switch (c) {
                // TODO: Figure out if we want these escaping sequences
                case '\'': c = '\''; break;
                case '\\': c = '\\'; break;
                case '"': c = '"'; break;
                case '?': c = '\?'; break;
                case 'a': c = '\a'; break;
                case 'b': c = '\b'; break;
                case 'f': c = '\f'; break;
                case 'n': c = '\n'; break;
                case 'r': c = '\r'; break;
                case 't': c = '\t'; break;
                case 'v': c = '\v'; break;
                default: continue;
            }

            p[i] = c;
            i++;
            c = *++source;
            continue;
        }

        p[i] = c;
        i++;
        c = *++source;
    }

    return s;
}

const char *process(const char *source) {
    const char *result = process_source(source);
    // TODO: Here we manage all the preprocessor instructions (like #include).
    // We need to come up with a way to ensure we write them separatedly without losing the
    // IP. Maybe use the same chunk and keep adding to it?
    return result;
}
