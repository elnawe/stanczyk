#include <stdio.h>
#include <string.h>

#include "memory.h"
#include "object.h"
#include "constant.h"

#define ALLOCATE_OBJECT(type, object_type)               \
    (type*)allocate_object(sizeof(type), object_type)

static Object* allocate_object(size_t size, ObjectType type) {
    Object* object = (Object*)reallocate(NULL, 0, size);
    object->type = type;
    return object;
}

static String *allocate_string(char* chars, int length) {
    String *string = ALLOCATE_OBJECT(String, OBJECT_STRING);
    string->length = length;
    string->chars = chars;
    return string;
}

String *copy_string(const char *chars, int length) {
    char *heapChars = ALLOCATE(char, length + 1);
    memcpy(heapChars, chars, length);
    heapChars[length] = '\0';
    return allocate_string(heapChars, length);

}
